/*Unlike pop shift() is used to remove the first element and shift other elements
  The shifted off value is assigned to removedFromMyArray*/

// Example
var ourArray = ["Stimpson", "J", ["cat"]];
var removedFromOurArray = ourArray.shift();
// removedFromOurArray now equals "Stimpson" and ourArray now equals ["J", ["cat"]].

